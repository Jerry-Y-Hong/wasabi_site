interface LanguageContextType {
    language: Language;
    setLanguage: (lang: Language) => void;
    t: (key: string, defaultValue?: string) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
    const [language, setLanguageState] = useState<Language>('ko');
    const [isMounted, setIsMounted] = useState(false);

    useEffect(() => {
        const saved = localStorage.getItem('kfarm_lang') as Language;
        if (saved) {
            setLanguageState(saved);
            // If it's not Korean, we might need a small delay for Google Translate to load and then trigger
            if (saved !== 'ko') {
                setTimeout(() => {
                    const gtCombo = document.querySelector('.goog-te-combo') as HTMLSelectElement;
                    if (gtCombo) {
                        gtCombo.value = saved;
                        gtCombo.dispatchEvent(new Event('change'));
                    }
                }, 100); // Small delay to ensure Google Translate widget is ready
            }
        }
        setIsMounted(true);
    }, []);

    const setLanguage = (lang: Language) => {
        setLanguageState(lang);
        localStorage.setItem('kfarm_lang', lang);
        if (typeof window !== 'undefined') {
            window.dispatchEvent(new Event('languagechange'));
        }
    };

    const t = (key: string, defaultValue?: string) => {
        // Always use 'ko' dictionary as the source for Google Translate to process
        const koDict = dictionary['ko'] as any;
        return koDict[key] || defaultValue || key;
    };

    return (
        <LanguageContext.Provider value={{ language, setLanguage, t }}>
            {/* Using key={language} forces React to re-mount the entire content tree when language changes */}
            <div key={language} style={{ display: 'contents' }}>
                {children}
            </div>
        </LanguageContext.Provider>
    );
}

export const useTranslation = () => {
    const context = useContext(LanguageContext);
    if (!context) throw new Error('useTranslation must be used within LanguageProvider');
    return context;
};
