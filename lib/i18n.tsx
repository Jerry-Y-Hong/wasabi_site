interface LanguageContextType {
    language: Language;
    setLanguage: (lang: Language) => void;
    t: (key: keyof typeof dictionary['ko']) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
    const [language, setLanguageState] = useState<Language>('ko');
    const [isMounted, setIsMounted] = useState(false);

    useEffect(() => {
        const saved = localStorage.getItem('kfarm_lang') as Language;
        if (saved && dictionary[saved]) {
            setLanguageState(saved);
        }
        setIsMounted(true);
    }, []);

    const setLanguage = (lang: Language) => {
        setLanguageState(lang);
        localStorage.setItem('kfarm_lang', lang);
        // Force a small update signal if needed
        if (typeof window !== 'undefined') {
            window.dispatchEvent(new Event('languagechange'));
        }
    };

    const t = (key: keyof typeof dictionary['ko']) => {
        const langDict = dictionary[language] || dictionary['ko'];
        return (langDict as any)[key] || (dictionary['ko'] as any)[key] || key;
    };

    return (
        <LanguageContext.Provider value={{ language, setLanguage, t }}>
            {/* Using key={language} forces React to re-mount the entire content tree when language changes */}
            <div key={language} style={{ display: 'contents' }}>
                {children}
            </div>
        </LanguageContext.Provider>
    );
}

export const useTranslation = () => {
    const context = useContext(LanguageContext);
    if (!context) throw new Error('useTranslation must be used within LanguageProvider');
    return context;
};
